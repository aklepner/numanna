class FME_Mediaappearance_Block_ProductVideos extends Mage_Catalog_Block_Product_Abstract
{

    const DISPLAY_CONTROLS = 'mediaappearance/general/displaycontrols';

    protected function _tohtml()
    {
        if ($this->getFromXml()=='yes'&&!Mage::getStoreConfig(self::DISPLAY_CONTROLS)) {
            return parent::_toHtml();
        }

        $this->setLinksforProduct();
        $this->setTemplate("mediaappearance/media2product.phtml");
        return parent::_toHtml();
    }
    
}
